# For loop in Python

# Example 1: loop through a list
numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num)


# Example 2: loop through a string
name = "Python"
for char in name:
    print(char)


# Example 3: using range()
for i in range(1, 6):
    print("Number:", i)


# Example 4: for loop with if condition
for i in range(1, 11):
    if i % 2 == 0:
        print("Even:", i)
